async function main() {
  // 既存記事があれば追加しない
  const count = await prisma.helpArticle.count()
  if (count > 0) {
    console.log(`既に ${count} 件のヘルプ記事があります。スキップします。`)
    return
  }

  for (const article of articles) {
    await prisma.helpArticle.create({ data: article })
  }
  console.log(`${articles.length} 件のヘルプ記事を作成しました。`)
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect())
